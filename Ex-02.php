<?php

//Правим две променливи за начало и край на цикъла
$start = 1;
$end = 30;
//Правим променлива за сбора на всички числа
$sum = 0;
//Правим цикъл който да върти от началото до края на зададените стойности.
for ($cols = $start; $cols <= $end; $cols++) {
//Прибавяме към променливата sum всяко едно от числата в цикъла
    $sum += $cols;
}
//Изписваме на конзолата сумата от чслата от началото до края
echo "Сумата от " . $start . " до " . $end . " = " . $sum;